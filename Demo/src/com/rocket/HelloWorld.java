package com.rocket;

public class HelloWorld {
    //   使用javac 命令编译java文件
//   使用java 命令运行java
    public static void main(String[] args) {
//     打印语句
        System.out.println("hello world!");
        System.out.println('☹');
//     制表符
        System.out.println("name" + "\t" + "age");
        System.out.println("Mary" + "\t" + "32");

//     变量 数据类型 变量名 = 数据值；
        int a = 10;
        int b = 15;
        int count = 0;
        System.out.println("结果：" + "\t" + (a + b));
        count += 1;
        count += 5;
        System.out.println("车上乘客：" + "\t" + count);

//

    }
}