package com.rocket;

//运算符
//整数参与计算，结果只能得到整数
// 小数参与计算，结果可能是不精确的
public class Operators {
    public static void main(String[] args) {
        System.out.println("运算符：" + 10 / 2);
        System.out.println("运算符：" + 10 / 3);
        System.out.println("运算符：" + 10.0 / 3);
//        取模
        System.out.println("取模：" + 10 % 2);
        System.out.println("取模：" + 10 % 3);
    }

}
